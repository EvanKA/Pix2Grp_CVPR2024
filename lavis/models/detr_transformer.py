def build_decoder(
        num_decoder_layers, d_model, return_intermediate_dec=False
):
    nhead = 8
    dim_feedforward = 2048
    dropout = 0.1
    activation = 'relu'
    normalize_before = True

    decoder_layer = TransformerDecoderLayer(
        d_model, nhead, dim_feedforward, dropout, activation, normalize_before
    )
    decoder_norm = nn.LayerNorm(d_model)
    decoder = TransformerDecoder(
        decoder_layer, num_decoder_layers, decoder_norm, return_intermediate=return_intermediate_dec,
    )
    return decoder

def build_encoder(
    num_decoder_layers, d_model
):
    nhead = 8
    dim_feedforward = 2048
    dropout = 0.1
    activation = 'relu'
    normalize_before = True

    decoder_layer = TransformerEncoderLayer(
        d_model, nhead, dim_feedforward, dropout, activation, normalize_before
    )
    decoder_norm = nn.LayerNorm(d_model)
    decoder = TransformerEncoder(
        decoder_layer, num_decoder_layers, decoder_norm, return_intermediate=False,
    )

    return decoder


